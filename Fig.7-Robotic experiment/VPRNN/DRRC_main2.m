global n ;

interval=0.005;                         %最小步长
jj=0;
epsilon=0;
for ii=1:length(t)
    if(t(ii,1)>=epsilon)                
        jj=jj+1;
        tn(jj,1)=t(ii,1);
        yn(jj,:)=y(ii,:);
        %%%%%%%%%%%%%%%%%
        doty=DRRC_net(tn(jj,1),yn(jj,:)');     %重新代入计算该时刻值神经网络输出
        ddqAll(jj,:)=doty((n+1):(2*n),1)';              %取出u_dot的前n个参数
        %%%%%%%%%%%%%%%%%
        epsilon=jj*interval;
    elseif(ii==length(t))
        jj=jj+1;
        tn(jj,1)=t(ii,1);
        yn(jj,:)=y(ii,:);
        %%%%%%%%%%%%%%%%%
        doty=DRRC_net(tn(jj,1),yn(jj,:)');
        ddqAll(jj,:)=doty((n+1):(2*n),1)';
        %%%%%%%%%%%%%%%%%
        epsilon=jj*interval;
    end
end
clear t y;
t=tn;
y=yn;
